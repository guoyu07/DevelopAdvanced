public class Shift
{
	public  static void main(String [] args)
	{
		int i=-1;
		System.out.println(Integer.toBinaryString(i));
		i>>>=10;
		System.out.println(i);
		System.out.println(Integer.toBinaryString(i));
		
		long l=-1;
		System.out.println(Long.toBinaryString(l));
		l>>>=10;
		System.out.println(l);
		System.out.println(Long.toBinaryString(l));

		
		
	
	}
}
